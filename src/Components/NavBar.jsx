import { IoMenu } from "react-icons/io5";
import React, { useEffect, useRef, useState } from "react";
import Logo from "../assets/Logo.png";
import OverlayMenu from "./OverlayMenu";

export default function NavBar() {
  const [menuOpen, setMenuOpen] = useState(false);
  const [visible, setVisible] = useState(true);
  const [forceVisible, setForceVisible] = useState(false);

  const lastScrollY = useRef(0);
  const timerId = useRef(null);

  useEffect(() => {
    const homeSection = document.querySelector("#Home");
    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting) {
          setForceVisible(true);
          setVisible(true);
        }
        else {
          setForceVisible (false)
        }
      }, {threshold : 0.1}
    )
    if (homeSection) observer.observe(homeSection)
      return () => {
    if(homeSection) observer.unobserve(homeSection);
    }

  },[])

  useEffect(() => {
    const handleScroll = () => {
      if(forceVisible){
        setVisible(true);
        return 
      }
      const currentScrolY = window.screenY;
      if (currentScrolY > lastScrollY.current){
        setVisible(false);
      }
      else{
        setVisible(true);

        if(timerId.current) clearTimeout(timerId.current);
        timerId.current = setTimeout(() => {
          setVisible(false)
        }, 3000)
      }
      lastScrollY.current = currentScrolY;
    }

    window.addEventListener("scroll", handleScroll, {passive:true})

    return () => {
      window.removeEventListener("scroll", handleScroll)

      if(timerId.current) clearTimeout(timerId.current)
    }
  }, [forceVisible])

  return (
    <>
      <nav
        className={`fixed top-0 left-0 w-full flex items-center justify-between px-6 py-6 z-50 
          transition-transform duration-300 ${visible ? "translate-y-0" : "-translate-y-full"
        }`}
      >
      
      {/* for logo and name */}
      <div className="flex items-center space-x-2">
        <img src={Logo} alt="logo" className="w-8 h-8" />
        <div className="text-2xl font-bold text-white hidden sm:block">Kunal</div>
      </div>

      {/* for menu icon  */}
      <div className="block lg:absolute lg:left-1/2 lg:transform lg:translate-x-1/2">
        <button 
        onClick={() => setMenuOpen(true)}
        className="text-white text-3xl focus:outline-none" 
        aria-label="open menu">
          <IoMenu/>
        </button>
      </div>


      {/* for Reach out button */}
      <div className="hidden lg:block"> 
        <a href="#ContactMe"
        className="bg-linear-to-r from-pink-500 to-blue-500 text-white px-5 py-2 rounded-full font-medium
        shadow-lg hover:opacity-90 transition-opacity duration-300 h-8 w-20"
        
        >
          Reach Out
        </a>
        
      </div>

      </nav>

      <OverlayMenu isOpen = {menuOpen} onClose ={() => setMenuOpen(false)}/>
    </>
  );
}
